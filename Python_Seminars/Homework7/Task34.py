# Винни - Пух считает, что ритм есть, если число слогов (т.е. число гласных букв) в каждой фразе стихотворения одинаковое. Фраза может состоять из одного слова, если во фразе несколько слов, то они разделяются дефисами. Фразы отделяются друг от друга пробелами.
# Стихотворение  Винни - Пух вбивает в программу с клавиатуры.
# В ответе напишите “Парам пам - пам”, если с ритмом все в порядке и “Пам парам”, если с ритмом все не в порядке

# * Пример:*

# ** Ввод: ** пара - ра - рам рам - пам - папам па - ра - па - да
# ** Вывод:** Парам пам - пам


# 1:
def vowel_count(my_str):
    vowel_list = [ 'а', 'и', 'е', 'ё', 'о', 'у', 'ы', 'э', 'ю', 'я']
    count = 0
    for i in my_str:
        if i in vowel_list: count += 1
    return count


# poem = 'пара-ра-рам рам-пам-папам па-ра-па-дам'
poem = input('введите стих: ')
poem = poem.split()
if len(set(map(vowel_count, poem))) == 1:
    print('Парам пам-пам')
else:
    print('Пам парам')   


